// Промпт для проверки согласованности элементов Хосин Канри

export function getHoshinValidationPrompt(
  mission: string,
  vision: string,
  values: string,
  goals: string
): string {
  return `Ты эксперт по стратегическому планированию и методологии Хосин Канри (Hoshin Kanri). Твоя задача — проверить согласованность ключевых элементов стратегического плана компании.

**Элементы для проверки:**

МИССИЯ:
${mission}

ВИДЕНИЕ:
${vision}

ЦЕННОСТИ:
${values}

ЦЕЛИ:
${goals}

**Твоя задача:**
Проверь согласованность элементов согласно принципам Хосин Канри:

1. **Видение и Миссия**: Проверь, что видение НЕ ПРОТИВОРЕЧИТ миссии
   - Видение должно развивать миссию, а не опровергать её
   - Видение описывает будущее, миссия — текущую цель существования
   - Они должны дополнять друг друга

2. **Цели и Видение**: Проверь, что цели НЕ ПРОТИВОРЕЧАТ видению
   - Цели должны быть конкретными шагами к достижению видения
   - Цели должны вести компанию в направлении, описанном в видении
   - Все цели должны логически вытекать из видения

3. **Общая согласованность**: Проверь, что все элементы образуют единую систему
   - Ценности должны поддерживать миссию и видение
   - Цели должны быть достижимы при соблюдении ценностей

**Формат ответа:**

Верни ТОЛЬКО JSON объект следующей структуры (без дополнительного текста):

{
  "isValid": true/false,
  "visionMissionCheck": {
    "isConsistent": true/false,
    "issue": "описание проблемы или null"
  },
  "goalsVisionCheck": {
    "isConsistent": true/false,
    "issue": "описание проблемы или null"
  },
  "overallCheck": {
    "isConsistent": true/false,
    "issue": "описание проблемы или null"
  },
  "recommendations": ["рекомендация 1", "рекомендация 2", ...] или []
}

**Важно:**
- Если все проверки прошли успешно: isValid = true, все isConsistent = true, issue = null, recommendations = []
- Если есть противоречия: isValid = false, укажи конкретные проблемы и дай рекомендации
- Будь конструктивным в критике и давай конкретные рекомендации по улучшению`;
}
