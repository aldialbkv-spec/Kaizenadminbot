export function getHoshinGoalsPrompt(originalText: string): string {
  return `Ты эксперт по стратегическому планированию и методологии Хосин Канри (Hoshin Kanri).

**Исходный текст целей:**
${originalText}

**Твоя задача:**
Проанализируй предоставленные цели и улучши их, следуя принципам Хосин Канри для годового планирования.

- Убедись, что цели соответствуют принципу SMART (конкретные, измеримые, достижимые, релевантные, ограниченные по времени)
- Цели должны отвечать на вопрос: "Чего мы хотим достичь в течение года?"
- Усиль фокус на измеримых результатах и метриках
- Убедись, что цели вытекают из миссии и видения
- Цели должны быть амбициозными, но достижимыми в течение 12 месяцев
- Включи временные рамки в пределах одного года (кварталы, месяцы, конкретные даты)
- Представь в виде списка из 3-5 ключевых целей с конкретными показателями

**Важно:** Верни ТОЛЬКО улучшенный текст целей без дополнительных комментариев, пояснений или форматирования.`;
}
