export function createQFDListsPrompt(companyDescription: string): string {
  return `Ты — эксперт по Quality Function Deployment (QFD) и анализу потребностей клиентов. Твоя задача — сгенерировать два ключевых списка для построения Дома качества.

ВХОДНЫЕ ДАННЫЕ:
Описание компании и продукта: ${companyDescription}

ЗАДАЧА:
Создай два списка в структурированном JSON формате. ВАЖНО: максимум 10 пунктов в каждом списке.

---

СПИСОК 1: ЧТО ХОЧЕТ КЛИЕНТ (Customer Requirements / WHATs)

Это ОЖИДАНИЯ и ОПЫТ, который хочет получить клиент. Формулируются на ЯЗЫКЕ КЛИЕНТА.

ПРИМЕРЫ ПРАВИЛЬНЫХ ФОРМУЛИРОВОК:
✅ "Быстрое получение результата"
✅ "Надежная работа без сбоев"
✅ "Простота в использовании"
✅ "Безопасность личных данных"
✅ "Удобный доступ в любое время"
✅ "Качественная поддержка при возникновении проблем"

ПРИМЕРЫ НЕПРАВИЛЬНЫХ ФОРМУЛИРОВОК (слишком технично):
❌ "Время отклика сервера менее 200мс"
❌ "99.9% uptime"
❌ "Количество кликов до цели"

ПРАВИЛА:
- Формулируй как ОЖИДАЕМЫЙ РЕЗУЛЬТАТ или ОПЫТ для клиента
- Используй простой язык, понятный клиенту (не технический жаргон)
- Описывай ЖЕЛАЕМОЕ СОСТОЯНИЕ, а не способ его достижения
- Фокусируйся на пользовательском опыте и восприятии
- Группируй по категориям: Функциональность, Удобство, Качество, Надежность, Безопасность, Поддержка
- НЕ включай требования связанные с ценой или стоимостью
- МАКСИМУМ 10 ПУНКТОВ

---

СПИСОК 2: ЧТО МЫ МОЖЕМ ДАТЬ (Technical Characteristics / HOWs)

Это ИЗМЕРИМЫЕ ПАРАМЕТРЫ ПРОДУКТА, которые компания контролирует для достижения требований клиентов.

ПРИМЕРЫ ПРАВИЛЬНЫХ ФОРМУЛИРОВОК:
✅ "Время отклика системы" (мс, ↓)
✅ "Доступность сервиса" (%, ↑)
✅ "Количество шагов для завершения задачи" (шт, ↓)
✅ "Уровень шифрования данных" (биты, ↑)
✅ "Количество доступных каналов связи" (шт, ↑)
✅ "Среднее время первого ответа поддержки" (минуты, ↓)
✅ "Частота обновлений ПО" (раз/месяц, ↑)
✅ "Объем обучающих материалов" (часов, ↑)

ПРИМЕРЫ НЕПРАВИЛЬНЫХ ФОРМУЛИРОВОК (дублируют требования клиентов):
❌ "Быстрая работа системы"
❌ "Удобный интерфейс"
❌ "Качественная поддержка"

ПРАВИЛА:
- Каждая характеристика должна быть ИЗМЕРИМОЙ с конкретной единицей измерения
- Это ТЕХНИЧЕСКИЕ ПАРАМЕТРЫ продукта/сервиса, которые компания напрямую контролирует
- Используй технический язык (для внутренней команды разработки/производства)
- Это должны быть ВНУТРЕННИЕ метрики продукта, а не восприятие клиента
- Характеристики должны влиять на требования клиентов, но НЕ дублировать их формулировки
- Группируй по направлениям: Производительность, Функциональность, Качество исполнения, Дизайн/UX, Надежность
- НЕ включай финансовые показатели (цена, себестоимость, ROI)
- МАКСИМУМ 10 ПУНКТОВ

НАПРАВЛЕНИЯ УЛУЧШЕНИЯ:
- "↑" = больше лучше (increase is better)
- "↓" = меньше лучше (decrease is better)
- "○" = есть оптимальное значение (target is best)

---

КЛЮЧЕВОЕ ОТЛИЧИЕ:
WHATs (требования клиентов) → Что клиент ЧУВСТВУЕТ и ХОЧЕТ получить
HOWs (характеристики) → Какие ПАРАМЕТРЫ ПРОДУКТА мы настраиваем, чтобы это обеспечить

Пример связки:
- Клиент хочет: "Быстрое получение результата" (WHAT)
- Мы обеспечиваем через: "Время обработки запроса" = 150мс (HOW), "Размер кэша" = 500МБ (HOW)

Пример связки:
- Клиент хочет: "Безопасность личных данных" (WHAT)
- Мы обеспечиваем через: "Уровень шифрования" = AES-256 (HOW), "Частота аудитов безопасности" = 4 раза/год (HOW)

---

ФОРМАТ ОТВЕТА (строго JSON):
{
  "customerRequirements": [
    {
      "id": "req1",
      "text": "Конкретное ожидание клиента",
      "category": "Функциональность"
    },
    ...
  ],
  "technicalCharacteristics": [
    {
      "id": "tech1",
      "text": "Конкретный измеримый параметр продукта",
      "category": "Производительность",
      "unit": "мс",
      "direction": "↓"
    },
    ...
  ]
}

ВАЖНО:
- Будь специфичным для данной индустрии и продукта
- Требования клиентов и технические характеристики должны логически связываться, но НЕ дублировать друг друга
- WHATs — это язык клиента (результат, опыт), HOWs — язык инженеров (параметры, метрики)
- Избегай общих фраз — каждый пункт должен быть конкретным
- Генерируй именно 7-10 пунктов в каждом списке (не больше 10!)
- Возвращай ТОЛЬКО валидный JSON без markdown разметки

Сгенерируй оба списка в формате JSON.`;
}
